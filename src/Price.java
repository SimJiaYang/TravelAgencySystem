abstract class Price{

    public abstract double getAdultPrice();

    public abstract double getChildWithExtraBedPrice();

    public abstract double getChildWithNoExtraBedPrice() ;

    public abstract double getInfantPrice();

    public abstract void setAdultPrice(double adultPrice);

    public abstract void setChildWithExtraBedPrice(double childWithExtraBedPrice);

    public abstract void setChildWithNoExtraBedPrice(double childWithNoExtraBedPrice) ;

    public abstract void setInfantPrice(double infantPrice);
}